public class Main {

    public static void main(String[] args) {
        int a = 4;
        int b = 5;
        System.out.println(a == b); //Igual
        System.out.println(a != b); // diferente
        System.out.println(a > 4); //maior que
        System.out.println(a < b); //menor que
        int c = 5;
        int d = 5;
        System.out.println(c > d); // maior que valores iguais
        System.out.println(c < d); // menor que valores iguais
        System.out.println(c >= d); //maior ou igual
        System.out.println(c <= d); //menor ou igual
    }
}